class CaffeineCatalogItem {
  const CaffeineCatalogItem({
    required this.id,
    required this.brand,
    required this.productName,
    required this.sizeLabel,
    required this.caffeineMg,
    required this.caffeineLabel,
    required this.locationHint,
    required this.sourceName,
    required this.sourceUrl,
    this.isEstimate = false,
    this.note = '',
  });

  final String id;
  final String brand;
  final String productName;
  final String sizeLabel;
  final int caffeineMg;
  final String caffeineLabel;
  final String locationHint;
  final String sourceName;
  final String sourceUrl;
  final bool isEstimate;
  final String note;

  String get displayName => '$productName $sizeLabel';

  String get submitName {
    final estimateTag = isEstimate ? '（估算）' : '';
    return '$brand $productName $sizeLabel$estimateTag';
  }
}

